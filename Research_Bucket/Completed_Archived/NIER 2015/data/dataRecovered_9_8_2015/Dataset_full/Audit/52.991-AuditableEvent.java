package org.oasis.ebxml.registry.infomodel;
import java.util.*;
import java.sql.Timestamp;

/**
 * AuditableEvent instances provide a long term record of events that effect a change of state in a RegistryObject. Such events are usually a result of a client initiated request. AuditableEvent instances are generated by the registry service to log such events.
 * <p>
 * Often such events effect a change in the life cycle of a RegistryObject. For example a client request could Create, Update, Deprecate or Delete a RegistryObject. No AuditableEvent is created for requests that do not alter the state of a RegistryObject. Specifically, read-only requests do not generate an AuditableEvent.
 * No AuditableEvent is generated for a RegistryObject when it is classified, assigned to a Package or associated with another Object.
 * <p>
 * A RegistryObject is associated with an ordered Collection of AuditableEvent instances that provide a complete audit trail for that Object.
 * @see
 * @author Farrukh S. Najmi
 * @version   1.1, 04/24/00
 */
public abstract class AuditableEvent extends RegistryObject {

    /**
     * The Timestamp for when this event occured.
     */
    public Timestamp timestamp;

    /**
     * The type of this event
     */
    public int eventType;

    /**
     * The RegistryObject associated with this AudiatbleEvent
     * @associates
     */
    public RegistryObject registryObject;

    /**
     * The User that sent the request that effected
     * this event.
     * 
     * @label requestor
     * @supplierCardinality 1
     * @directed
     * @associates <{User}>
     */
    public User user;

    /**
     * @link aggregation
     * @supplierCardinality 1..*
     * @supplierRole affectedObjects 
     */
    private RegistryObject lnkRegistryObject;
}
